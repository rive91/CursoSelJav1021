package ejemplos_if;

public class IfElseIf_Ejemplo {

	public static void main(String[] args) {
		int i = 8;
		
		if(i == 10) {
			System.out.println("i es 10");
		}else if(i == 15){
			System.out.println("i es 15");
		}else if(i == 20) {
			System.out.println("i es 20");
		}else {
			System.out.println("i no es equivalente a las condiciones anteriores");
		}
			

	}

}
