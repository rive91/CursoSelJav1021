package ejemplos_ciclos;

public class For_ejemplo {

	public static void main(String[] args) {
		int y = 10;
		
		//bucle for comienza cuando x  = 0
		//'X' es una variable local dentro del 'FOR'
		//'for' va a correr hasta que y <=5
		for(int x = 5; x<=y;x++) {
			System.out.println("Valor de x: " + x);
		}

	}

}
