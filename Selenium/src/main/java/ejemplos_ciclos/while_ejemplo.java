package ejemplos_ciclos;

public class while_ejemplo {

	public static void main(String[] args) {
		int a = 11;
		
		//Salir del bucle cuando 'a' es mayor que 4
		while (a <= 10) {
			System.out.println("el valor de A: " + a);
			
			//incrementa el valor de 'a' para la siguiente iteracion
			a++;
		}

	}

}
